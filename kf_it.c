void __attribute__((interrupt))_USART0_exception (void)
{
	if(USART_Get_Receive_BUFR_Ready_Flag(USART0_SFR))
	{
		if(USART_ReceiveData(USART0_SFR)==0x5A)
		{
			Receive_flag=1;
		}
	}
}


